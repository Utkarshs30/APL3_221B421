// RedHeadDuck.java
public class RedHeadDuck implements Duck, Flyable, Quackable {

    public void swim() {
        System.out.println("RedHead Duck is swimming gracefully!");
    }

    public void fly() {
        System.out.println("RedHead Duck is flying!");
    }


    public void quack() {
        System.out.println("RedHead Duck quacks!");
    }
}
